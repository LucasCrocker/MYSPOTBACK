const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const userValidation = require('../../validations/user.validation');
const userController = require('../../controllers/user.controller');
const drivewayValidation = require('../../validations/driveway.validation');

const router = express.Router();

router
  .route('/')
  .post(auth('manageUsers'), validate(userValidation.createUser), userController.createUser)
  .get(auth('getUsers'), validate(userValidation.getUsers), userController.getUsers);

router
  .route('/register-driveway')
  .post(auth('blanket'), validate(drivewayValidation.registerDriveway), userController.addDrivewayToUser);

  
router
  .route('/update-driveway')
  .post(auth('blanket'), validate(drivewayValidation.updateDriveway), userController.updateDriveway);

// router
//   .route('/booking-status')
//   .get(auth('blanket'), validate(drivewayValidation.getBookingStatus), userController.getBookingStatus);

  // get driveways (only POST so we can submit location info)
router
  .route('/driveways')
  .post(auth('blanket'), validate(drivewayValidation.getDriveways), userController.getDriveways);

router
  .route('/set-schedule')
  .post(auth('blanket'), validate(drivewayValidation.setSchedule), userController.setDaySchedule);

router
  .route('/book-driveway')
  .post(auth('blanket'), validate(drivewayValidation.bookDriveway), userController.bookDriveway);

router
  .route('/release-driveway')
  .get(auth('blanket'), userController.releaseDriveway);

router
  .route('/account-status')
  .get(auth('blanket'), userController.accountStatus);

router
  .route('/account-link')
  .get(auth('blanket'), userController.accountLink);

router
  .route('/payment-sheet')
  .get(auth('blanket'), userController.paymentSheet);

router
  .route('/check-for-payment-method')
  .get(auth('blanket'), userController.checkForPaymentMethod);

router
  .route('/update-payment-method')
  .get(auth('blanket'), userController.updatePaymentMethod);

router
  .route('/delete-driveway')
  .get(auth('blanket'), userController.deleteDriveway);

router
  .route('/report-user')
  .post(auth('blanket'), userController.reportUser);  

router
  .route('/register-as-driver')
  .post(auth('blanket'), validate(userValidation.registerUserAsDriver), userController.registerUserAsDriver);

router
  .route('/:userId')
  .get(auth('getUsers'), validate(userValidation.getUser), userController.getUser)
  .patch(auth('manageUsers'), validate(userValidation.updateUser), userController.updateUser)
  .delete(auth('manageUsers'), validate(userValidation.deleteUser), userController.deleteUser);

module.exports = router;

